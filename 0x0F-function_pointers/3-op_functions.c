#include "3-calc.h"

/**
 * op_add - adds two integer
 * @a: first int
 * @b: second int
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub two integer
 * @a: first int
 * @b: second int
 * Return: the sum
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiple two integer
 * @a: first int
 * @b: second int
 * Return: the sum
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div two integer
 * @a: first int
 * @b: second int
 * Return: the sum
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - multiple two integer
 * @a: first int
 * @b: second int
 * Return: the sum
 */
int op_mod(int a, int b)
{
	return (a % b);
}
